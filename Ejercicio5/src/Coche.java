public class Coche {

    String marca;
    String modelo;
    String color;
    int puertas;
    boolean airbags;

    public Coche(){}

    public Coche(String marca, String modelo, String color, int puertas, boolean airbags) {
        this.marca = marca;
        this.modelo = modelo;
        this.color = color;
        this.puertas = puertas;
        this.airbags = airbags;
    }
}
